// various operations on assignment operator
import java.util.*;
class assopera
{
public static void main(String args[])
{
Scanner sc = new Scanner(System.in);
System.out.println("Enter any number");
int a= sc.nextInt();
a+=3;  //a=a+3
System.out.println(a);
a-=4;  // a=a-3
System.out.println(a);
a/=3; //a=a/3
System.out.println(a);
a*=4; //a=a*4
System.out.println(a);
}
}
